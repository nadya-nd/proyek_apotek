        'name' => 'required|unique:medicines,name',
        'medicine_stock' => 'required|integer',
        'medicine_prize' => 'required|numeric',
    ]);

    Medicine::create($validated);

    // return redirect()->route('obat.index')->with('success', 'Obat berhasil ditambahkan.');
